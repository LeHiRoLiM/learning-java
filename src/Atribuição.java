public class Atribuição
{
    public static void main(String[] args)
    {
//        somar e atribuir
        int a = 2;
        a += 3;
        System.out.println(a);

//        subtrair e atribuir
        int b = 22;
        b -= 3;
        System.out.println(b);

//        multiplicar e atribuir
        int c = 4;
        c *= 2;
        System.out.println(c);

//        dividir e atribuir
        int d = 10;
        d /= 2;
        System.out.println(d);

//        resto e atribuir
        int e = 1;
        e %= 8;
        System.out.println(e);
    }
}
